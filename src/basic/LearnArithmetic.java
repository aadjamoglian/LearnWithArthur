package basic;

public class LearnArithmetic {

	public static void main(String[] args) {
		
		int a = 2;
		int b = 5;
		
		int total = a + b;
		
		System.out.println(total);
		
		int x = 10;
		int y = 20;
		
		int totalxy = x - y;
		
		System.out.println(totalxy);
		
		double c = 10;
		double d = 3;
		
		double totalcd = (c/d);
		
		System.out.println(totalcd);
		
		int z = 5;
		
		z = z + 1;
		System.out.println(z);
		
		z += 1; // z = z + 1
		System.out.println(z);
		
		z++;
		System.out.println(z);

	}

}
