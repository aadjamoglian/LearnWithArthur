package basic;

public class EscapeSequences {

	public static void main(String[] args) {
		
		System.out.println("Hello my name is Arthur.");
		System.out.println("Hello my name \tis Arthur.");
		System.out.println("Hello my name is Arthur.\b1");
		System.out.println("Hello my name is \nArthur.");
		System.out.println("Hello my \"name\" is Arthur.");
		System.out.println("Hello my \'name\' is Arthur.");
		System.out.println("Hello my \rname is Arthur.");

	}

}
